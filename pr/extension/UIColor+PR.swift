import UIKit

extension UIColor {
    class func prBlack() -> UIColor {
        return UIColor(red:0.08, green:0.08, blue:0.08, alpha:1.0)
    }
    
    class func prGray() -> UIColor {
        return UIColor(red:0.68, green:0.68, blue:0.68, alpha:1.0)
    }
}
